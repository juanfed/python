# otro ejecicio basico de aritmetica con python

calculo1 = 17 / 19;
print(calculo1); # esto me dará muchos decimales

print("{:.2f}".format(calculo1)) # me imprimirá solo los 2 deciamles sigueintes
print("{:.5f}".format(calculo1)) # puedo restringir el numero de decimals que mostraré
print("{:.0f}".format(calculo1)) # me imprimirá la parte entera, pero me redondea el numero
print("{:.1f}".format(calculo1)) # me imprimirá solo un numero decimal, sin redondear


