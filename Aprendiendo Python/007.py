# un poco de operaciones aritmeticas con python

# divicion entera

divicion = 40 / 13;
divicionEntera = 40 // 13;
print(f"Divicion con su parte decimal {divicion}")
print(f"Divicion si su parte decimal {divicionEntera}")

# divicion de residuo 

divicionDeResiduo = 40 % 13;
print(f"dicicion que me trae la parte decimal 0 o 1, solo trae cero si el resultado es exapto{divicionDeResiduo}");

# potenciacion 

potenciacion = 8 ** 2;
print(f"Potencicion de 8 elevado al cuadrado es de: {potenciacion}");